//************************************************************************************
// Include libraries
#include <Arduino.h>
#include "serialEEPROMwriter.h"
//************************************************************************************
// Global variables
uint32_t baudRate = 115200;
//************************************************************************************
// Initialize instance
serialEEPROMwriter prom;
//************************************************************************************
// Setup
void setup() {
    prom.begin(baudRate);
}
//************************************************************************************
// Main loop
void loop() {
    prom.readSerial();
}
